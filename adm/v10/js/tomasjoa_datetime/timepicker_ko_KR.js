function timePicker(object,v_type,f_time,t_time,u_val,s_time){
	var obj = object;
	var val = u_val;
	timeArr = (s_time == 30 || !s_time) ? timeArr30 : timeArr10;
	var tp = 0;
	var apm = 2;
	var opts = '';
	var relArr = new Array();
	var indexFrom = 0;
	var indexTo = 0;
	var view_type = (v_type == 12) ? v_type : 24;
	var from_time = (f_time > 0) ? f_time : 0;
	var to_time = (t_time < 23) ? t_time : 23;
	console.log(timeArr);
	if(from_time > to_time){
		alert('시작시간이 종료시간보다 클수는 없습니다.');
		return false;
	}
	
	if(view_type == '12'){
		tp = 1;
	}else{
		tp = 0;
	}
	
	indexFrom = fromTimeIndex(from_time);
	indexTo = toTimeIndex(to_time);
	for(var j = indexFrom; j<=indexTo; j++){
		//relArr.push(bwg_timeArr[j]);
		//console.log(bwg_timeArr[j]);
		opts += "<option value='"+timeArr[j][0]+"'"+((val == timeArr[j][0])?' selected="selected"':'')+">"+timeArr[j][tp]+((tp) ? "("+timeArr[j][2]+")" : "")+"</option>";
	}
	
	obj.html(opts).addClass('bwg_time');
}

function fromTimeIndex(f){
	var idx = 0;
	for(var i in timeArr){
		if(Number(timeArr[i][0].substr(0,2)) == f){
			idx = i;
			break;
		}
	}
	
	return idx;
}

function toTimeIndex(t){
	var idx = timeArr.length - 1;
	for(var i=idx; i>=0; i--){
		if(Number(timeArr[i][0].substr(0,2)) == t){
			idx = i;
			break;
		}
	}
	
	return idx;
}